#include <cores\arm\include\cpu.h>
#include <netmf_watchdog.h>


HRESULT Watchdog_Enable( UINT32 TimeoutMilliseconds, WATCHDOG_INTERRUPT_CALLBACK isr, void* context )
{
    return WatchDog_Driver::Enable( TimeoutMilliseconds, isr, context );
}

void Watchdog_Disable()
{
    WatchDog_Driver::Disable();
}

void Watchdog_ResetCounter()
{
    WatchDog_Driver::ResetCounter();
}

void Watchdog_ResetCpu()
{
    WatchDog_Driver::ResetCpu();
}